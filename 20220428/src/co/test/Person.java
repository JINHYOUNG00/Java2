package co.test;

public class Person {
	//필드
	private String ssn;
	private String name;
	private String address;
	
	
	//기본생성자

	
	//메소드
	public String getSsn() {
		return ssn;
	}
	public void setSsn(String ssn) {
		this.ssn = ssn;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		address = address;
	}
	
	
	
	
	
	
}
